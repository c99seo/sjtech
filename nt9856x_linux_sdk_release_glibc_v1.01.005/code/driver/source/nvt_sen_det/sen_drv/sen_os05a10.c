static void sen_set_flip_os05a10(CTL_SEN_ID id, CTL_SEN_FLIP *flip)
{
	CTL_SEN_CMD cmd;
	UINT32 start_x = 0;
	ER rt = E_OK;
	
	if ((CTL_SEN_MODE_9 == (cur_sen_mode[id])) || (CTL_SEN_MODE_12 == (cur_sen_mode[id]))) {
		if (!first_rote_flag[id]) {		
			cmd = sen_set_cmd_info_os05a10(0x3811, 1, 0x0, 0x0);
			rt |= sen_read_reg_os05a10(id, &cmd);
			start_x_ori[id] = cmd.data[0];
			first_rote_flag[id] = 1;
		}	
	}

	cmd = sen_set_cmd_info_os05a10(0x3821, 1, 0x0, 0x0);
	rt |= sen_read_reg_os05a10(id, &cmd);

	if (basic_param.property & CTL_SEN_SUPPORT_PROPERTY_MIRROR) {
		if (*flip & CTL_SEN_FLIP_H) {
				cmd.data[0] |= 0x4;
				if ((CTL_SEN_MODE_9 == (cur_sen_mode[id])) || (CTL_SEN_MODE_12 == (cur_sen_mode[id]))) {		
					start_x = start_x_ori[id] - 1;
				}
		} else {
				cmd.data[0] &= (~0x04);
				if ((CTL_SEN_MODE_9 == (cur_sen_mode[id])) || (CTL_SEN_MODE_12 == (cur_sen_mode[id]))) {		
					start_x = start_x_ori[id];
				}				
		}
	} else {
		DBG_WRN("no support mirror \r\n");
	}
	
	rt |= sen_write_reg_os05a10(id, &cmd);
	
	if ((CTL_SEN_MODE_9 == (cur_sen_mode[id])) || (CTL_SEN_MODE_12 == (cur_sen_mode[id]))) {
		cmd = sen_set_cmd_info_os05a10(0x3811, 1, start_x, 0x0);
		rt |= sen_write_reg_os05a10(id, &cmd);
	}

	cmd = sen_set_cmd_info_os05a10(0x3820, 1, 0x0, 0x0);
	rt |= sen_read_reg_os05a10(id, &cmd);

	if (basic_param.property & CTL_SEN_SUPPORT_PROPERTY_FLIP) {
		if (*flip & CTL_SEN_FLIP_V) {
			cmd.data[0] |= 0x4;
		} else {
			cmd.data[0] &= (~0x04);
		}
	} else {
		DBG_WRN("no support flip \r\n");
	}

	rt |= sen_write_reg_os05a10(id, &cmd);

	if (rt != E_OK) {
		DBG_ERR("write register error %d \r\n", (INT)rt);
	}
}

static ER sen_get_flip_os05a10(CTL_SEN_ID id, CTL_SEN_FLIP *flip)
{
	CTL_SEN_CMD cmd;
	ER rt = E_OK;

	cmd = sen_set_cmd_info_os05a10(0x3821, 1, 0x0, 0x0);
	rt |= sen_read_reg_os05a10(id, &cmd);
	*flip = CTL_SEN_FLIP_NONE;
	if (cmd.data[0] & 0x4) {
		*flip |= CTL_SEN_FLIP_H;
	}

	cmd = sen_set_cmd_info_os05a10(0x3820, 1, 0x0, 0x0);
	rt |= sen_read_reg_os05a10(id, &cmd);
	if (cmd.data[0] & 0x4) {
		*flip |= CTL_SEN_FLIP_V;
	}

	return rt;
}


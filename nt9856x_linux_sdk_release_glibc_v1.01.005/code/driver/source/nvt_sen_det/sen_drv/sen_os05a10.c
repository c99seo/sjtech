static void sen_ext_get_probe_sen_os05a10(CTL_SEN_ID id, CTL_SENDRV_GET_PROBE_SEN_PARAM *data)
{

    CTL_SEN_CMD cmd;
    UINT32 read_data_h = 0,read_data_m1 = 0, read_data_m2 = 0, read_data_l = 0,sensor_id = 0;
    ER rt = E_OK;

    cmd = sen_set_cmd_info_os05a10(0x6000, 1,0x0, 0x0);
    rt |= sen_read_reg_os05a10(id, &cmd);
    read_data_h = cmd.data[0];
    if( rt != E_OK) {
        data->probe_rst = rt;
        return;
    }

    cmd = sen_set_cmd_info_os05a10(0x6001, 1, 0x0, 0x0);
    rt |= sen_read_reg_os05a10(id, &cmd);
    read_data_m1 = cmd.data[0];

    if( rt != E_OK) {
        data->probe_rst = rt;
        return;
    }


    cmd = sen_set_cmd_info_os05a10(0x6002,1, 0x0, 0x0);
    rt |= sen_read_reg_os05a10(id, &cmd);
    read_data_m2 = cmd.data[0];

    if( rt != E_OK) {
        data->probe_rst = rt;
        return;
    }


    cmd = sen_set_cmd_info_os05a10(0x6003, 1,0x0, 0x0);
    rt |= sen_read_reg_os05a10(id, &cmd);
    read_data_l = cmd.data[0];

    if( rt != E_OK) {
        data->probe_rst = rt;
        return;
    }

    sensor_id = ((read_data_h & 0xFF) << 24) | ((read_data_m1 & 0xFF) << 16) | ((read_data_m2 & 0xFF) << 8) | (read_data_l & 0xFF);

    //printk("sensor id = %x\r\n",sensor_id);
    if( rt != E_OK) {
        data->probe_rst = rt;
    }
    else if (SENSOR_ID == sensor_id) {
        data->probe_rst = 0;
    } else if (SENSOR_ID != sensor_id) {
        data->probe_rst = -1;
    }
}


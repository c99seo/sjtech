static void sen_get_modesel_os05a10(CTL_SENDRV_GET_MODESEL_PARAM *data)
{
	if (data->if_type != CTL_SEN_IF_TYPE_MIPI) {
		DBG_ERR("if_type %d N.S. \r\n", data->if_type);
		return;
	}

	if (data->data_fmt != CTL_SEN_DATA_FMT_RGB) {
		DBG_ERR("data_fmt %d N.S. \r\n", data->data_fmt);
		return;
	}

	if (data->data_lane == CTL_SEN_DATALANE_2) {
		if ((data->size.w <= 2592) && (data->size.h <= 1944)) {
			if (data->frame_rate <= 1500) {
				data->mode = CTL_SEN_MODE_8;
				return;
			} else if (data->frame_rate <= 3000) {
				data->mode = CTL_SEN_MODE_12;
				return;
			}	
		}
	} else { //	if (data->data_lane == CTL_SEN_DATALANE_4) {	
		if (data->frame_num == 1) {
			if ((data->size.w == 2560) && (data->size.h == 1920)) {	
				if (data->frame_rate <= 3000) {
					data->mode = CTL_SEN_MODE_4;
					return;
				}
			} else {
				if ((data->size.w <= 2688) && (data->size.h <= 1944)) {
					if ((data->size.w <= 2592) && (data->size.h <= 1944)) {
						if ((data->size.w <= 2592) && (data->size.h <= 1456)) {
							if ((data->size.w <= 2560) && (data->size.h <= 1440)) {
								if ((data->size.w <= 1920) && (data->size.h <= 1080)) {
									if (data->frame_rate <= 12000) {
										data->mode = CTL_SEN_MODE_3;
										return;
									}
								} else {
									if (data->frame_rate <= 3000) {
										data->mode = CTL_SEN_MODE_11;
										return;
									}
								}
							} else {
								if (data->frame_rate <= 6000) {
									data->mode = CTL_SEN_MODE_5;
									return;
								}
							}
						}	else {
							if (data->frame_rate <= 3000) {
								data->mode = CTL_SEN_MODE_9;
								return;							
							} else {
								data->mode = CTL_SEN_MODE_1;
								return;								
							}
						}
					}	else {
						if (data->frame_rate <= 3000) {
							data->mode = CTL_SEN_MODE_7;
							return;
						}				
					}
				}
			}
		} else if (data->frame_num == 2) {
			if ((data->size.w <= 2592) && (data->size.h <= 1944)) {
				if ((data->size.w <= 2592) && (data->size.h <= 1456)) {
					if ((data->size.w <= 2560) && (data->size.h <= 1440)) {
						if (data->frame_rate <= 3000) {
							data->mode = CTL_SEN_MODE_10;
							return;
						}
					} else {
						if (data->frame_rate <= 3000) {
							data->mode = CTL_SEN_MODE_6;
							return;
						}
					}
				} else {
					if (data->frame_rate <= 3000) {
						data->mode = CTL_SEN_MODE_2;
						return;
					}
				}
			}
		}
	}

	DBG_ERR("fail (frame_rate%d,size%d*%d,if_type%d,data_fmt%d,frame_num%d) \r\n"
			, data->frame_rate, data->size.w, data->size.h, data->if_type, data->data_fmt, data->frame_num);
	data->mode = CTL_SEN_MODE_1;
}

static UINT32 sen_calc_rowtime_step_os05a10(CTL_SEN_ID id, CTL_SEN_MODE mode)
{
	UINT32 div_step = 0;

	if (mode >= SEN_MAX_MODE) {
		mode = cur_sen_mode[id];
	}

	if ((mode_basic_param[mode].mode_type == CTL_SEN_MODE_STAGGER_HDR) || (mode_basic_param[mode].mode_type == CTL_SEN_MODE_LINEAR))	{
		div_step = 1;
	}/* else {
		div_step = 1;
	}
*/
	return div_step;
}

static UINT32 sen_calc_rowtime_os05a10(CTL_SEN_ID id, CTL_SEN_MODE mode)
{
	UINT32 row_time = 0;

	if (mode >= SEN_MAX_MODE) {
		mode = cur_sen_mode[id];
	}

	//Precision * 10
	if (mode_basic_param[mode].mode_type == CTL_SEN_MODE_STAGGER_HDR) {
		row_time = 10 * (mode_basic_param[mode].signal_info.hd_period) / ((speed_param[mode].pclk) / 2 / 1000000);
	} else {
		row_time = 10 * (mode_basic_param[mode].signal_info.hd_period) / ((speed_param[mode].pclk) / 1000000);
	}

	return row_time;
}

static void sen_get_rowtime_os05a10(CTL_SEN_ID id, CTL_SENDRV_GET_MODE_ROWTIME_PARAM *data)
{
	data->row_time_step = sen_calc_rowtime_step_os05a10(id, data->mode);
	data->row_time = sen_calc_rowtime_os05a10(id, data->mode) * (data->row_time_step);;
}


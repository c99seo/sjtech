static UINT32 sen_calc_chgmode_vd_os05a10(CTL_SEN_ID id, UINT32 fps)
{
	UINT32 sensor_vd;

	if (1 > fps) {
		DBG_ERR("sensor fps can not small than (%d),change to dft sensor fps (%d) \r\n", fps, mode_basic_param[cur_sen_mode[id]].dft_fps);	
		fps = mode_basic_param[cur_sen_mode[id]].dft_fps;
	}
	sensor_vd = (mode_basic_param[cur_sen_mode[id]].signal_info.vd_period) * (mode_basic_param[cur_sen_mode[id]].dft_fps) / fps;

	sen_set_chgmode_fps_os05a10(id, fps);
	sen_set_cur_fps_os05a10(id, fps);

	if (sensor_vd > MAX_VD_PERIOD) {
		DBG_ERR("sensor vd out of sensor driver range (%d) \r\n", sensor_vd);
		sensor_vd = MAX_VD_PERIOD;
		fps = (mode_basic_param[cur_sen_mode[id]].signal_info.vd_period) * (mode_basic_param[cur_sen_mode[id]].dft_fps) / sensor_vd;
		sen_set_chgmode_fps_os05a10(id, fps);
		sen_set_cur_fps_os05a10(id, fps);
	}

	if(sensor_vd < (mode_basic_param[cur_sen_mode[id]].signal_info.vd_period)) {
		DBG_ERR("sensor vd out of sensor driver range (%d) \r\n", sensor_vd);
		sensor_vd = mode_basic_param[cur_sen_mode[id]].signal_info.vd_period;
		sen_set_chgmode_fps_os05a10(id, mode_basic_param[cur_sen_mode[id]].dft_fps);
		sen_set_cur_fps_os05a10(id, mode_basic_param[cur_sen_mode[id]].dft_fps);
	}

	return sensor_vd;
}

static UINT32 sen_calc_exp_vd_os05a10(CTL_SEN_ID id, UINT32 fps)
{
	UINT32 sensor_vd;

	if (1 > fps) {
		DBG_ERR("sensor fps can not small than (%d),change to dft sensor fps (%d) \r\n", fps, mode_basic_param[cur_sen_mode[id]].dft_fps);	
		fps = mode_basic_param[cur_sen_mode[id]].dft_fps;
	}
	sensor_vd = (mode_basic_param[cur_sen_mode[id]].signal_info.vd_period) * (mode_basic_param[cur_sen_mode[id]].dft_fps) / fps;

	if (sensor_vd > MAX_VD_PERIOD) {
		DBG_ERR("sensor vd out of sensor driver range (%d) \r\n", sensor_vd);
		sensor_vd = MAX_VD_PERIOD;
	}

	if(sensor_vd < (mode_basic_param[cur_sen_mode[id]].signal_info.vd_period)) {
		DBG_ERR("sensor vd out of sensor driver range (%d) \r\n", sensor_vd);
		sensor_vd = mode_basic_param[cur_sen_mode[id]].signal_info.vd_period;
	}

	return sensor_vd;
}

static void sen_set_gain_os05a10(CTL_SEN_ID id, void *param)
{
	ISP_SENSOR_CTRL *sensor_ctrl = (ISP_SENSOR_CTRL *)param;
	UINT32 data1[ISP_SEN_MFRAME_MAX_NUM] = {0};
	UINT32 data2[ISP_SEN_MFRAME_MAX_NUM] = {0};
	UINT32 data3[ISP_SEN_MFRAME_MAX_NUM] = {0};
	UINT32 data4[ISP_SEN_MFRAME_MAX_NUM] = {0};	
	UINT32 frame_cnt, total_frame;
	CTL_SEN_CMD cmd;
	UINT32 data = 0;
	UINT32 a_gain = 0, d_gain = 0, blc_trigger = 0, trigger_value = 0, awb_gain = 0;
	ER rt = E_OK;

	sensor_ctrl_last[id].gain_ratio[0] = sensor_ctrl->gain_ratio[0];
	sensor_ctrl_last[id].gain_ratio[1] = sensor_ctrl->gain_ratio[1];

	// Calculate sensor gain
	if (mode_basic_param[cur_sen_mode[id]].frame_num == 0) {
		DBG_WRN("total_frame = 0, force to 1 \r\n");
		total_frame = 1;
	} else {
		total_frame = mode_basic_param[cur_sen_mode[id]].frame_num;
	}

	for (frame_cnt = 0; frame_cnt < total_frame; frame_cnt++) {
		if (sensor_ctrl->gain_ratio[frame_cnt] < (mode_basic_param[cur_sen_mode[id]].gain.min)) {
			sensor_ctrl->gain_ratio[frame_cnt] = (mode_basic_param[cur_sen_mode[id]].gain.min);
		} else if (sensor_ctrl->gain_ratio[frame_cnt] > (mode_basic_param[cur_sen_mode[id]].gain.max)) {
			sensor_ctrl->gain_ratio[frame_cnt] = (mode_basic_param[cur_sen_mode[id]].gain.max);
		}

		if ( 6400 < (sensor_ctrl->gain_ratio[frame_cnt])) {
			blc_trigger = 0;
		} else {
			blc_trigger = 1;
		}
	
		if ( 15500 >= sensor_ctrl->gain_ratio[frame_cnt] ) {
			a_gain= (sensor_ctrl->gain_ratio[frame_cnt]) * 128 / 1000;
			d_gain= 0x0400;
			awb_gain = 0x400;
		} else if ( 248000 >= sensor_ctrl->gain_ratio[frame_cnt] ) {
			a_gain= 0x07c0;			
			d_gain= (sensor_ctrl->gain_ratio[frame_cnt]) * 1024 / 15500;
			awb_gain = 0x400;			
		} else {
			a_gain= 0x07c0;				
			d_gain= 0x3FFF;
			awb_gain = (sensor_ctrl->gain_ratio[frame_cnt]) * 1024 / 248000;
		}

		if (0x3FFF < d_gain) { 
			d_gain = 0x3FFF;
		}

		if (0xFFF < awb_gain) { 
			awb_gain = 0xFFF;
		}

		data1[frame_cnt] = a_gain;
		data2[frame_cnt] = d_gain;
		data3[frame_cnt] = blc_trigger;
		data4[frame_cnt] = awb_gain;		
	}

	if (mode_basic_param[cur_sen_mode[id]].mode_type == CTL_SEN_MODE_LINEAR) {

		// Write sensor gain
		//Set analog gain
		cmd = sen_set_cmd_info_os05a10(0x3508, 1, ((data1[0]) >> 8) & 0x07, 0x0);
		rt |= sen_write_reg_os05a10(id, &cmd);
		cmd = sen_set_cmd_info_os05a10(0x3509, 1, (data1[0])& 0xFF, 0x0);
		rt |= sen_write_reg_os05a10(id, &cmd);

		//Set digital gain
		cmd = sen_set_cmd_info_os05a10(0x350A, 1, ((data2[0]) >> 8) & 0x3F, 0x0);
		rt |= sen_write_reg_os05a10(id, &cmd);
		cmd = sen_set_cmd_info_os05a10(0x350B, 1, (data2[0])& 0xFF, 0x0);
		rt |= sen_write_reg_os05a10(id, &cmd);
		
		//Set awb gain
		cmd = sen_set_cmd_info_os05a10(0x5100, 1, ((data4[0]) >> 8) & 0x0F, 0x0);
		rt |= sen_write_reg_os05a10(id, &cmd);
		cmd = sen_set_cmd_info_os05a10(0x5101, 1, (data4[0])& 0xFF, 0x0);
		rt |= sen_write_reg_os05a10(id, &cmd);
		
		cmd = sen_set_cmd_info_os05a10(0x5102, 1, ((data4[0]) >> 8) & 0x0F, 0x0);
		rt |= sen_write_reg_os05a10(id, &cmd);
		cmd = sen_set_cmd_info_os05a10(0x5103, 1, (data4[0])& 0xFF, 0x0);
		rt |= sen_write_reg_os05a10(id, &cmd);
				
		cmd = sen_set_cmd_info_os05a10(0x5104, 1, ((data4[0]) >> 8) & 0x0F, 0x0);
		rt |= sen_write_reg_os05a10(id, &cmd);
		cmd = sen_set_cmd_info_os05a10(0x5105, 1, (data4[0])& 0xFF, 0x0);
		rt |= sen_write_reg_os05a10(id, &cmd);
				
	} else if (mode_basic_param[cur_sen_mode[id]].mode_type == CTL_SEN_MODE_STAGGER_HDR) {
		//Set long analog gain
		cmd = sen_set_cmd_info_os05a10(0x3508, 1, ((data1[0]) >> 8 & 0x07 ), 0x0);
		rt |= sen_write_reg_os05a10(id, &cmd);
		cmd = sen_set_cmd_info_os05a10(0x3509, 1, data1[0] & 0xFF, 0x0);
		rt |= sen_write_reg_os05a10(id, &cmd);
		//Set short analog gain
		cmd = sen_set_cmd_info_os05a10(0x350C, 1, ((data1[1]) >> 8 & 0x07 ), 0x0);
		rt |= sen_write_reg_os05a10(id, &cmd);
		cmd = sen_set_cmd_info_os05a10(0x350D, 1, data1[1] & 0xFF, 0x0);
		rt |= sen_write_reg_os05a10(id, &cmd);

		//Set long digital gain
		cmd = sen_set_cmd_info_os05a10(0x350A, 1, ( data2[0] >> 8 & 0x3F ), 0x0);
		rt |= sen_write_reg_os05a10(id, &cmd);
		cmd = sen_set_cmd_info_os05a10(0x350B, 1, data2[0] & 0xFF, 0x0);
		rt |= sen_write_reg_os05a10(id, &cmd);

		//Set short digital gain
		cmd = sen_set_cmd_info_os05a10(0x350E, 1, ( data2[1] >> 8 & 0x3F ), 0x0);
		rt |= sen_write_reg_os05a10(id, &cmd);
		cmd = sen_set_cmd_info_os05a10(0x350F, 1, data2[1] & 0xFF, 0x0);
		rt |= sen_write_reg_os05a10(id, &cmd);
		
		//Set awb gain
		cmd = sen_set_cmd_info_os05a10(0x5100, 1, ((data4[0]) >> 8) & 0x0F, 0x0);
		rt |= sen_write_reg_os05a10(id, &cmd);
		cmd = sen_set_cmd_info_os05a10(0x5101, 1, (data4[0])& 0xFF, 0x0);
		rt |= sen_write_reg_os05a10(id, &cmd);
		
		cmd = sen_set_cmd_info_os05a10(0x5102, 1, ((data4[0]) >> 8) & 0x0F, 0x0);
		rt |= sen_write_reg_os05a10(id, &cmd);
		cmd = sen_set_cmd_info_os05a10(0x5103, 1, (data4[0])& 0xFF, 0x0);
		rt |= sen_write_reg_os05a10(id, &cmd);
				
		cmd = sen_set_cmd_info_os05a10(0x5104, 1, ((data4[0]) >> 8) & 0x0F, 0x0);
		rt |= sen_write_reg_os05a10(id, &cmd);
		cmd = sen_set_cmd_info_os05a10(0x5105, 1, (data4[0])& 0xFF, 0x0);
		rt |= sen_write_reg_os05a10(id, &cmd);
		
		cmd = sen_set_cmd_info_os05a10(0x5140, 1, ((data4[1]) >> 8) & 0x0F, 0x0);
		rt |= sen_write_reg_os05a10(id, &cmd);
		cmd = sen_set_cmd_info_os05a10(0x5141, 1, (data4[1])& 0xFF, 0x0);
		rt |= sen_write_reg_os05a10(id, &cmd);
		
		cmd = sen_set_cmd_info_os05a10(0x5142, 1, ((data4[1]) >> 8) & 0x0F, 0x0);
		rt |= sen_write_reg_os05a10(id, &cmd);
		cmd = sen_set_cmd_info_os05a10(0x5143, 1, (data4[1])& 0xFF, 0x0);
		rt |= sen_write_reg_os05a10(id, &cmd);
				
		cmd = sen_set_cmd_info_os05a10(0x5144, 1, ((data4[1]) >> 8) & 0x0F, 0x0);
		rt |= sen_write_reg_os05a10(id, &cmd);
		cmd = sen_set_cmd_info_os05a10(0x5145, 1, (data4[1])& 0xFF, 0x0);
		rt |= sen_write_reg_os05a10(id, &cmd);			
	}
	//Set BLC trigger
	cmd = sen_set_cmd_info_os05a10(0x4000, 1, 0x00, 0x0);

	rt |= sen_read_reg_os05a10(id, &cmd);
	data = cmd.data[0];

	if (data3[0]) {
		trigger_value = data & 0x7F;
	} else {
		trigger_value = data | 0x80;
	}

	if ( data != trigger_value) { 
		cmd = sen_set_cmd_info_os05a10(0x4000, 1, trigger_value, 0x0);
		rt |= sen_write_reg_os05a10(id, &cmd);
	}

	if (rt != E_OK) {
		DBG_ERR("write register error %d \r\n", (INT)rt);
	}
}

static void sen_set_expt_os05a10(CTL_SEN_ID id, void *param)
{
	ISP_SENSOR_CTRL *sensor_ctrl = (ISP_SENSOR_CTRL *)param;
	UINT32 line[ISP_SEN_MFRAME_MAX_NUM];
	UINT32 frame_cnt, total_frame;
	CTL_SEN_CMD cmd;
	UINT32 expt_time = 0, sensor_vd = 0, chgmode_fps = 0, cur_fps = 0, clac_fps = 0, t_row = 0;
	UINT32 l2s_distance = 0, y_out_size = 0;
	ER rt = E_OK;

	sensor_ctrl_last[id].exp_time[0] = sensor_ctrl->exp_time[0];
	sensor_ctrl_last[id].exp_time[1] = sensor_ctrl->exp_time[1];

	if (mode_basic_param[cur_sen_mode[id]].frame_num == 0) {
		DBG_WRN("total_frame = 0, force to 1 \r\n");
		total_frame = 1;
	} else {
		total_frame = mode_basic_param[cur_sen_mode[id]].frame_num;
	}

	// Calculate exposure line
	for (frame_cnt = 0; frame_cnt < total_frame; frame_cnt++) {
		// Calculates the exposure setting
		t_row = sen_calc_rowtime_os05a10(id, cur_sen_mode[id]);
		if (0 == t_row) {
			DBG_WRN("t_row  = 0, must >= 1 \r\n");
			t_row = 1;
		}
		line[frame_cnt] = (sensor_ctrl->exp_time[frame_cnt]) * 10 / t_row;

		// Limit minimun exposure line
		if (line[frame_cnt] < MIN_EXPOSURE_LINE) {
			line[frame_cnt] = MIN_EXPOSURE_LINE;
		}
	}

	// Write exposure line
	// Get fps
	chgmode_fps = sen_get_chgmode_fps_os05a10(id);

	// Calculate exposure time
	t_row = sen_calc_rowtime_os05a10(id, cur_sen_mode[id]);
	if (mode_basic_param[cur_sen_mode[id]].mode_type == CTL_SEN_MODE_STAGGER_HDR) {
		expt_time = (line[0] + line[1]) * t_row / 10;
	} else {
		expt_time = (line[0]) * t_row / 10;
	}

	// Calculate fps
	if (0 == expt_time) {
		DBG_WRN("expt_time  = 0, must >= 1 \r\n");		
		expt_time = 1;
	}
	clac_fps = 100000000 / expt_time;

	cur_fps = (clac_fps < chgmode_fps) ? clac_fps : chgmode_fps;
	sen_set_cur_fps_os05a10(id, cur_fps);

	// Calculate new vd
	sensor_vd = sen_calc_exp_vd_os05a10(id, cur_fps);

	//Check max vts
	if (sensor_vd > MAX_VD_PERIOD) {
		DBG_ERR("max vts overflow\r\n");
		sensor_vd = MAX_VD_PERIOD;
	}

	cmd = sen_set_cmd_info_os05a10(0x380E, 1, (sensor_vd >> 8) & 0xFF, 0);
	rt |= sen_write_reg_os05a10(id, &cmd);

	cmd = sen_set_cmd_info_os05a10(0x380F, 1, sensor_vd & 0xFF, 0);
	rt |= sen_write_reg_os05a10(id, &cmd);

	if (mode_basic_param[cur_sen_mode[id]].mode_type == CTL_SEN_MODE_LINEAR) {
		//Check max exp line reg
		if (line[0] > (MAX_VD_PERIOD-NON_EXPOSURE_LINE)) {
			DBG_ERR("max line overflow \r\n");
			line[0] = (MAX_VD_PERIOD-NON_EXPOSURE_LINE);
		}

		//Check min exp line
		if (line[0] < MIN_EXPOSURE_LINE) {
			DBG_ERR("min line overflow\r\n");
			line[0] = MIN_EXPOSURE_LINE;
		}

		//Check max exp line
		if (line[0] > (sensor_vd - NON_EXPOSURE_LINE)) {
			line[0] = sensor_vd - NON_EXPOSURE_LINE;
		}

		// set exposure line to sensor
		cmd = sen_set_cmd_info_os05a10(0x3501, 1, ( line[0] >> 8 ) & 0xFF, 0);
		rt |= sen_write_reg_os05a10(id, &cmd);
		cmd = sen_set_cmd_info_os05a10(0x3502, 1, line[0] & 0xFF , 0);
		rt |= sen_write_reg_os05a10(id, &cmd);
	} else if (mode_basic_param[cur_sen_mode[id]].mode_type == CTL_SEN_MODE_STAGGER_HDR) {
		if ((mode_basic_param[cur_sen_mode[id]].frame_num) == 2) {
			if (line[0] > (sensor_vd - HDR_NON_EXPOSURE_LINE)) {
				line[0] = sensor_vd - HDR_NON_EXPOSURE_LINE;
			}

			if (line[1] > (sensor_vd - HDR_NON_EXPOSURE_LINE)) {
				line[1] = sensor_vd - HDR_NON_EXPOSURE_LINE;
			}

			if (line[1] >= line[0]) {
				DBG_ERR("error T1(%d) must be > T2(%d)\r\n", line[0], line[1]);
			}

			if (((line[1]) + sensor_vd) >= MAX_VD_PERIOD) {
				if(sensor_vd < 0xFFFA) {
					line[1] = MAX_VD_PERIOD - sensor_vd -1;
				} else {
					sensor_vd = 0xFFFA;
					line[1] = 4;
				}
			}

			if ((line[1]) > 255) {
				if ((line[1] + 8) >= MAX_VD_PERIOD ) {
					l2s_distance = MAX_VD_PERIOD;
				} else {
					l2s_distance = line[1] + 8;
				}
			} else {
				l2s_distance = line[1] + 2;
			}

			y_out_size = (mode_basic_param[cur_sen_mode[id]].act_size->h)+(mode_basic_param[cur_sen_mode[id]].act_size->y);
		
			if (l2s_distance > sensor_vd - (y_out_size+BLACKLINE+L2SOFFSET)) {
				l2s_distance = sensor_vd - (y_out_size+BLACKLINE+L2SOFFSET);
			}

			if (((line[0])+l2s_distance+4)>= sensor_vd) {
				line[0] = sensor_vd-l2s_distance-5;
			}

			if (sensor_vd <= ((line[0])+(line[1])+8)) {
				line[1] = sensor_vd - line[0] - 9;
			}

			//Set long exposure line to sensor
			cmd = sen_set_cmd_info_os05a10(0x3501, 1, ( line[0] >> 8 ) & 0xFF, 0);
			rt |= sen_write_reg_os05a10(id, &cmd);
			cmd = sen_set_cmd_info_os05a10(0x3502, 1, ( line[0] & 0xFF), 0);
			rt |= sen_write_reg_os05a10(id, &cmd);
			//Set short exposure line to sensor
			cmd = sen_set_cmd_info_os05a10(0x3511, 1, ( line[1] >> 8 ) & 0xFF, 0);
			rt |= sen_write_reg_os05a10(id, &cmd);
			cmd = sen_set_cmd_info_os05a10(0x3512, 1, ( line[1] & 0xFF), 0);
			rt |= sen_write_reg_os05a10(id, &cmd);

			//Set L2S-distance
			cmd = sen_set_cmd_info_os05a10(0x3798, 1, ( l2s_distance >> 8 ) & 0xFF, 0);
			rt |= sen_write_reg_os05a10(id, &cmd);
			cmd = sen_set_cmd_info_os05a10(0x3799, 1, l2s_distance & 0xFF, 0);
			rt |= sen_write_reg_os05a10(id, &cmd);
		}
	}
	if (rt != E_OK) {
		DBG_ERR("write register error %d \r\n", (INT)rt);
	}
}

